
package model;


public class Raza {
    
    private String razaId, razaDescrip, razaEst, razaEspID, razaDesEspecie;

    public String getRazaId() {
        return razaId;
    }

    public void setRazaId(String razaId) {
        this.razaId = razaId;
    }

    public String getRazaDescrip() {
        return razaDescrip;
    }

    public void setRazaDescrip(String razaDescrip) {
        this.razaDescrip = razaDescrip;
    }

    public String getRazaEst() {
        return razaEst;
    }

    public void setRazaEst(String razaEst) {
        this.razaEst = razaEst;
    }

    public String getRazaEspID() {
        return razaEspID;
    }

    public void setRazaEspID(String razaEspID) {
        this.razaEspID = razaEspID;
    }

    public String getRazaDesEspecie() {
        return razaDesEspecie;
    }

    public void setRazaDesEspecie(String razaDesEspecie) {
        this.razaDesEspecie = razaDesEspecie;
    }



   
    
    
  
}